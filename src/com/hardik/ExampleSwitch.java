package com.hardik;

public class ExampleSwitch {

	public static void main(String[] args) {
		int x = 2;
		switch(x)
		{
		case 1 :
			System.out.println(x);
			break;
		case 2 :
			System.out.println("this is Number" + x);
			break;
		default :
			System.out.println("Default");
			break;
		}
		
		

	}

}
